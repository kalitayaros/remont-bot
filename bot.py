async def main():
    logging.basicConfig(level=logging.WARNING)
    print("Bot v2.0 zapushchen")
    print("Kanal: " + CHANNEL_ID)
    print("Postov v plane: " + str(len(POSTS)))
    state = load_state()
    print("Sleduyushchiy post: #" + str(state["post_index"] + 1))

    scheduler = AsyncIOScheduler(timezone="Europe/Moscow")
    for t in POST_TIMES:
        h, m = map(int, t.split(":"))
        scheduler.add_job(scheduled_post, trigger="cron", hour=h, minute=m)
        print("Raspisaniye: " + t + " MSK")

    scheduler.start()
    print("Planirovshchik zapushchen. Ctrl+C dlya ostanovki.")
    try:
        while True:
            await asyncio.sleep(60)
    except (KeyboardInterrupt, SystemExit):
        scheduler.shutdown()
        print("Bot ostanovlen")

