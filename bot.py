def load_state():
    if os.path.exists(STATE_FILE):
        with open(STATE_FILE, encoding="utf-8") as f:
            return json.load(f)
    return {"post_index": 0}

def save_state(state):
    with open(STATE_FILE, "w", encoding="utf-8") as f:
        json.dump(state, f, ensure_ascii=False, indent=2)

async def publish_post(bot, post):
    try:
        if post.get("type") == "photo" and post.get("image"):
            msg = await bot.send_photo(
                chat_id=CHANNEL_ID,
                photo=post["image"],
                caption=post["text"],
                parse_mode="HTML"
            )
        else:
            msg = await bot.send_message(
                chat_id=CHANNEL_ID,
                text=post["text"],
                parse_mode="HTML"
            )

        print("OK den " + str(post["day"]) + " / " + post["slot"])

        if post.get("first_comment"):
            await asyncio.sleep(3)
            await bot.send_message(
                chat_id=CHANNEL_ID,
                text=post["first_comment"],
                reply_to_message_id=msg.message_id
            )

    except TelegramError as e:
        print("OSHIBKA: " + str(e))
        # Если фото не загрузилось — публикуем текст
        if post.get("type") == "photo":
            try:
                await bot.send_message(
                    chat_id=CHANNEL_ID,
                    text=post["text"],
                    parse_mode="HTML"
                )
            except TelegramError as e2:
                print("OSHIBKA TEXT: " + str(e2))

async def scheduled_post():
    state = load_state()
    idx = state["post_index"]
    if idx >= len(POSTS):
        print("Vse posty opublikovany!")
        return
    bot = Bot(token=BOT_TOKEN)
    await publish_post(bot, POSTS[idx])
    state["post_index"] = idx + 1
    save_state(state)

